// Implicit conversion entails changing a smaller datatype to a larger datatype
public class implicit_conversion {
    public static void main(String[] args) {
        // integer
        int num1 = 2500;
        // double (decimal format)
        double num2 = num1;

        System.out.println("Initial int value is " + num1);
        System.out.println("Int value changed to double " + num2);

    }
}
