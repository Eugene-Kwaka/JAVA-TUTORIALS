public class divisionOperator {
    public static void main(String[] args){
        int a = 10;
        int b = 3;
        System.out.println(a / b); // The result will be an int 3 instead of decimal 3.333333333

        double num1= 13;
        double num2 = 3;
        System.out.println(num1 / num2); // The result will be a decimal 4.33333333
    }
}

